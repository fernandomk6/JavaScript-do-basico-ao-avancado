/**
 * Rotas
 * Nos sites e nas APIs temos o que chamamos de todas.
 * Que é basicamente a URL que queremos acessar.
 * Porém quando tratamos de uma API chamados as URLs 
 * de rota.
 * No Express, vamos criar as nossas rotas para serem
 * acessadas via HTTP, e responderem baseadas numa
 * lógica que inserirmos.
 * Cada rota vai receber um método HTTP, que deve
 * coincidir com o proposto pelo REST, conforme
 * vimos anteriormente.
 */