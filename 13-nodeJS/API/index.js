/**
 * API o que é?
 * Application Programming Interface
 * Uma maneira de se comunicar via http a um servidor
 * e fazer operações como visualizar, deletar, criar
 * e atualizar dados.
 * Grandes aplicações como FAcebook e Instagram utilizam
 * APIs.
 * Grande parte dos software desenvolvidos em Node.JS
 * tem uma API e utilizam um framework chamado Express
 * para isso.
 */

/**
 * O que é REST e RESTFul?
 * Quando falamos em API estamos condicionados a falar de REST
 * e RESTFul também.
 * REST = Representational State Transfer, princípios e 
 * regras que permitem criar uma aplicação com interfaces
 * bem definidas.
 * RESTFul = Capacidade de aplicar os princípios do REST
 */

/**
 * REST e os verbos HTTP
 * Sempre que criamos uma API devemos utilizar os verbos
 * HTTP corretos para cada URL da nossa aplicação
 * (veremos isso na prática).
 * Verbo GET para solicitar dados.
 * Verbo POST para inserir dados.
 * Verbo DELETE para deletar dados.
 * Verbo PUT para atualizar dados.
 */